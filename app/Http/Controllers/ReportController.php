    }

    public function list()
    {
        if (!Auth::check()) return redirect('/login');
        $this->authorize('list', Report::class);
        $reports = Auth::registered_user()->reports()->orderBy('id');
        return view('pages.reports', ['reports' => $reports]);
    }

